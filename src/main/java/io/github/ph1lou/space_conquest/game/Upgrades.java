package io.github.ph1lou.space_conquest.game;

public class Upgrades {

    private int punch=0;

    private int chestPlate =0;

    private int protection=0;

    private int sharpness=0;


    public int getChestPlate() {
        return chestPlate;
    }

    public void setChestPlate(int chestPlate) {
        this.chestPlate = chestPlate;
    }

    public int isProtection() {
        return protection;
    }

    public void setProtection(int protection) {
        this.protection = protection;
    }

    public int isSharpness() {
        return sharpness;
    }

    public void setSharpness(int sharpness) {
        this.sharpness = sharpness;
    }

    public int isPunch() {
        return punch;
    }

    public void setPunch(int punch) {
        this.punch = punch;
    }



}
